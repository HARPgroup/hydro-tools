    #' @description Creates a plot of the the daily historic flow percentiles,
    #'   in bands from 100, 95, 90, 75, 50, 25, 10, 5, and 0. Also plots the
    #'  daily flow, to show where the flow is in term sof historic flows of that
    #'  day. This uses \code{WaterGageStats()} to retrieve the percentiles
    #' @param start_date The lower bound of the daily flows to put on the plot, in YYYY-MM-DD.
    #'  If more than#' a year is selected, the percentiles repeat (i.e. the bands for 
    #'  2024-01-01 will be the #' same as the bands for 2025-01-01)
    #' @param end_date The upper bound of daily flows to show, in YYYY-MM-DD.. Defaults to 
    #' today's date
    #' @return A plot showing the daily flow and the percentile bands for each day
    plot_percentiles = function(start_date = (Sys.Date() - 7), end_date = Sys.Date()) {
      ## Check if the stats are already run
      if (!inherits(self$stats_obj,"WaterGageStats") && is.na(self$stats_obj)) {
        self$get_doy_percentiles()
      } 
      
      #Get the stats data and store locally
      stat_df <- self$stats_obj$gage_data
      
      #Reorganize data into wide style data frame
      stat_df_wide <- stat_df |> 
        dplyr::select(time_of_year, value, percentile, computation) |> 
        #Drop geometry for reorganization
        sf::st_drop_geometry() |> 
        tidyr::pivot_wider(id_cols = time_of_year,
                           names_from = percentile,
                           values_from = value)
      
      ## Pull gage data in the date range
      daily <- self$gage_data[self$gage_data[,self$date_col] > start_date &
                                self$gage_data[,self$date_col] < end_date,]
      
      ## Attach stats to a date df that limits it to the date range,
      ## but repeats if the range covers multiple years
      days_df <- data.frame(date = seq.Date(as.Date(start_date), 
                                            as.Date(end_date), by = "day"))
      
      ## Adding a %m-%d column to join on stats
      days_df$mmdd <- format(days_df$date,"%m-%d")
      
      stats_df_plot <- sqldf::sqldf("
        SELECT days_df.date,
        stats.*
        FROM days_df 
        LEFT JOIN stat_df_wide stats
          ON stats.time_of_year  = days_df.mmdd
      ")
      
      
      # Fill bins
      bins <- c("95 - Max", "90 - 95", "75 - 90", "25 - 75", 
                "10 - 25", "5 - 10", "Min - 5")
      bins <- factor(bins,levels = bins)
      
      g <- ggplot2::ggplot(data = stats_df_plot, ggplot2::aes(x = date)) +
        ggplot2::geom_ribbon(ggplot2::aes(ymin = `95`, ymax = `100`, fill = bins[1]), na.rm = TRUE) +
        ggplot2::geom_ribbon(ggplot2::aes(ymin = `90`, ymax = `95`, fill = bins[2]), na.rm = TRUE) +
        ggplot2::geom_ribbon(ggplot2::aes(ymin = `75`, ymax = `90`, fill = bins[3]), na.rm = TRUE) +
        ggplot2::geom_ribbon(ggplot2::aes(ymin = `25`, ymax = `75`,  fill = bins[4]), na.rm = TRUE) +
        ggplot2::geom_ribbon(ggplot2::aes(ymin = `10`, ymax = `25`,  fill = bins[5]), na.rm = TRUE) +
        ggplot2::geom_ribbon(ggplot2::aes(ymin = `5`,  ymax = `10`,  fill = bins[6]), na.rm = TRUE) +
        ggplot2::geom_ribbon(ggplot2::aes(ymin = `0`,  ymax = `5`,   fill = bins[7]), na.rm = TRUE) +
        ggplot2::geom_line(ggplot2::aes(y = `50`, linetype = "dashed"), linewidth = .35,
                           color = "black", na.rm = TRUE) +
        ggplot2::geom_line(data = daily, ggplot2::aes(x = !!sym(self$date_col),
                                                      y = !!sym(self$flow_col),
                                                      linetype = 'solid'),
                           linewidth = 2, color = "black") +
        ggplot2::scale_y_log10() +
        ## Limits the number of months shown on x axis
        ggplot2::guides(x = ggplot2::guide_axis(check.overlap = TRUE)) +
        ggplot2::labs(x = "Date",y = "Discharge (cfs)") +
        ggplot2::theme_bw() +
        ## Coloring the ribbons
        ggplot2::scale_fill_manual("Historical Percentiles",
                                   values = c("95 - Max" = "#292f6b",
                                              "90 - 95" = "#5699c0",
                                              "75 - 90" = "#aacee0",
                                              "25 - 75" = "#e9e9e9",
                                              "10 - 25" = "#ebd6ab",
                                              "5 - 10" = "#dcb668",
                                              "Min - 5" = "#8f4f1f"),
                                   labels = c("95th Percentile - Max",
                                              "90th - 95th Percentile",
                                              "75th - 90th Percentile",
                                              "25th - 75th Percentile",
                                              "10th - 25th Percentile",
                                              "5th - 10th Percentile",
                                              "Min - 5th Percentile")) +
        ## Adding a legend to the line type
